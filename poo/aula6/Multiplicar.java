public class Multiplicar { 
    public static void main(String[] args) {
        int i = 7;
        System.out.println("Dobro dos numeros");        
        System.out.println(i);
        i = i * 2;
        System.out.println(i);
        // i = i * 2;
        i *= 2;
        System.out.println(i);
        i *= 2;
        System.out.println(i);

    }
}