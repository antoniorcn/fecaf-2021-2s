let numero = 800;

let iguais2 = numero == "800";  // true
let iguais3 = numero === "800";  // false
let diferente1 = numero != "800";  // false 
let diferente2 = numero !== "800";  // true 

console.log('Valor 800 e "800" são: ');
console.log("São iguais (==) ==> ", iguais2);
console.log("São iguais (===) ==> ", iguais3);

console.log("São diferentes (!=) ==> ", diferente1);
console.log("São diferentes (!==) ==> ", diferente2);
